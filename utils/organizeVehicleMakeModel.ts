export const organizeCarsByMake = (data) => {
  const organizedData = {};

  data.forEach((car) => {
    const [make, ...model ] = car.name.split(" ");
    const finalModelName = model.join(' ');

    if (!organizedData[make]) {
      organizedData[make] = {
        logo: "https://storefront-consumer-assets.s3.ap-south-1.amazonaws.com/static/plan/return-to-invoice-bike.svg",
        models: []
      }; 
    }
    if (organizedData[make].models.indexOf(finalModelName) == -1) {
      organizedData[make].models = [
        ...organizedData[make].models,
        {
           model_name: finalModelName, variants: [] 
        }
      ];
    }
    organizedData[make].models.forEach(modeln => {
      if (modeln?.model_name === finalModelName) { modeln?.variants?.push(...car?.variants); }
    })
  });

  return organizedData;
};

