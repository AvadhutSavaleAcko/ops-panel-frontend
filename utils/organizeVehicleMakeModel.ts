export const formatVariantsDataByFuelType = (makeName, modelName, MMV_DATA, defaultVariant) => {
  if (!makeName || !modelName || isEmpty(MMV_DATA)) {
    return defaultVariant ? {name: defaultVariant} : null;
  }
  const makeData = MMV_DATA?.[makeName];
  console.log('shared mmv-data', {makeName, modelName, makeData, MMV_DATA, defaultVariant, makeData})
  const modelData = makeData?.models?.filter(data => data?.model_name === modelName);
  const formattedVariants = modelData?.[0]?.variants?.reduce(
    (acc, currvariant) => {
      const { fuel_type } = currvariant;
      if (!acc[fuel_type]) {
        acc[fuel_type] = [];
      }
      acc[fuel_type].push(currvariant);
      return acc;
    },
    {}
  );

  return formattedVariants;

}
